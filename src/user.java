    public void getDataUS(){
        //kosongkan tabel
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        //eksekusi koneksi dan kirimkan query ke database
        try{
            //tes koneksi
            Statement stat = (Statement) koneksiDB.getKoneksi().createStatement();
            
            //perintah sql untuk membaca data dari tabel operator        
            String sql = "SELECT * FROM user";
            ResultSet US = stat.executeQuery(sql);
            
            //baca data
            while(US.next()){
                //membuat obyek berjenis array
                Object[] obj = new Object[7];
                obj[0]=US.getString("id_user");
                obj[1]=US.getString("nama_user");
                obj[2]=US.getString("alamat");
                obj[3]=US.getString("no_telp");
                obj[4]=US.getString("username");
                obj[5]=US.getString("password");
                obj[6]=US.getString("hak_akses");
                model.addRow(obj);
            }
        }catch(SQLException err){
           JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }
    
